package HomeWorkClass13;

public class WhileLoopEvenOdd {
    public static void main(String[] args){

        // To print even numbers

        int i = 0;
        while (i < 10) {
            System.out.println(i * 2);
            i++;
        }

      // To print odd numbers

      int x = 1;
        while(x < 22){
            System.out.println(+x);
            x = x + 2;


        }

    }
}
