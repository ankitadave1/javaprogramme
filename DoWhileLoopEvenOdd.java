package HomeWorkClass13;

public class DoWhileLoopEvenOdd {
    //Print even number
    public static void main(String[] args){

        int a = 20;
        do{
            System.out.println(a * 2);
            a++;
        }
        while(a<=30);


        //Print odd number

        int b = 15;
        do{
            System.out.println(b);
            b = b + 2;
        }
        while(b<=35);



    }
}
